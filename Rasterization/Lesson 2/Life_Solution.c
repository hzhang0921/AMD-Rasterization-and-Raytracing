/*
 * init_current()
 */
void init_current( void )
{

#if INIT_RANDOM

    for( int j = 0; j < 800; j++ )
    {
		for( int i = 0; i < 800; i++ )
		{
			current[j][i] = (random_float(0,100) > 90) ? 1 : 0;
		}
	}
	
#else

    int height = 99;
    int width  = 50;

	for (int columns = 0; columns < height; columns++) {
		for (int rows = 0; rows < width; rows++) {
			reverseLinepuffer[columns][rows] = linepuffer[columns][50-rows];
		}
	}

	for (int columns = 0; columns < 76; columns++) {
		for (int rows = 0; rows < 77; rows++) {
			reversefourenginecordership[columns][rows] = fourenginecordership[columns][77-rows];
		}
	}

    for( int j = 0; j < height; j++ )
    {
		for( int i = 0; i < width; i++ )
		{
			current[j + 200][i + 200] = linepuffer[j][i];
			current[j + 200][i + 600] = reverseLinepuffer[j][i];
		}
    }
	for (int j = 0; j < 76; j++) {
		for (int i = 0; i < 77; i++) {
			current[j + 400][i + 200] = fourenginecordership[j][i];
			current[j + 400][i + 600] = reversefourenginecordership[j][i];
		}
	}
#endif

}

/*
 * copy_next_to_current()
 */
void copy_next_to_current( void )
{
    for( int j = 0; j < 800; j++ )
    {
		for( int i = 0; i < 800; i++ )
		{
			current[j][i] = next[j][i];
		}
	}
}

/*
 * count_neighbors()
 */
int count_neighbors( int i, int j )
{
	int num_neighbors = current[j-1][i-1] + current[j-1][i] + current[j-1][i+1] +
						current[j  ][i-1]                   + current[j  ][i+1] +
						current[j+1][i-1] + current[j+1][i] + current[j+1][i+1];
						
	return( num_neighbors );
}

/*
 * calc_next()
 *
 * 1. Any live cell with fewer than two live neighbours dies, as if by underpopulation.
 * 2. Any live cell with two or three live neighbours lives on to the next generation.
 * 3. Any live cell with more than three live neighbours dies, as if by overpopulation.
 * 4. Any dead cell with exactly three live neighbours becomes a live cell, as if by reproduction
 */
void calc_next( void )
{
    for( int j = 1; j < 799; j++ )
    {
		for( int i = 1; i < 799; i++ )
        {
			int neighbors = count_neighbors( i, j );

			if( current[j][i] == 1 )  // alive cell case
			{
				if( neighbors == 2 || neighbors == 3 )
					next[j][i] = 1;
				else
					next[j][i] = 0;
			}
			else                      // dead cell case
			{
				if( neighbors == 3 )
					next[j][i] = 1;
				else
					next[j][i] = 0;
			}
		}
    }
}

/*
 * draw_current()
 */
void draw_current( void )
{
    set_color( 1, 1, 1, 1 );

    for( int j = 0; j < 800; j++ )
    {
		for( int i = 0; i < 800; i++ )
        {
	    	if( current[j][i] )
	    	{
	    		draw_point( i-400, j-400 );
	    	}
        }
    }
}

/*
 * draw_life()
 */
void draw_life( void )
{
	draw_current();
	calc_next();
	copy_next_to_current();
	
	generations++;
}

